package acm634;

public class Calculator {
	
	public double sum(double x, double y){
		
		return x + y;
	}
	
	public double substraction(double z, double w){
		
		return z - w;
	}
	
	public double multiplication(double q, double r){
		
		return q * r;
		
	}
	
	public double division(double a, double b){
		
		return a / b;
	}

}
